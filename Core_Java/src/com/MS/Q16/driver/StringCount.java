package com.MS.Q16.driver;


public class StringCount {

	/*
	 * The main method has a string input as the main method argument. It
	 * then spits out said string input along with how many chars said string is.
	 */
	public static void main(String[] args) {
		System.out.println(args[0].toString());
		System.out.println(args[0].length() + " characters long");
	}

}
