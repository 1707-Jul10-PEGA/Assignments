package com.dpq11b.driver;

public class FloatStorage {
	
	private float x;
	private float y;

	public FloatStorage() {
		x = 7.0f;
		y = 317.314f;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
}
