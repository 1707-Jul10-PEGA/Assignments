package com.bc.q11.driver.p1;

public class FloatClass {
	private float first;
	private float second;
	public FloatClass(){
		setFirst(0);
		setSecond(0);
	}
	public float getSecond() {
		return second;
	}
	public void setSecond(float second) {
		this.second = second;
	}
	public float getFirst() {
		return first;
	}
	public void setFirst(float first) {
		this.first = first;
	}
}
